    public HashMap<Integer, String> getData(SQLiteDatabase mydb) {
        Cursor res =  mydb.rawQuery( "select * from BuildsTable limit 100", null );
        res.moveToFirst();
        HashMap<Integer, String> map = new HashMap<>();
        while(res.isAfterLast() == false){
            map.put(res.getInt(res.getColumnIndex("Price")), res.getString(res.getColumnIndex("CPU")));
            res.moveToNext();
        }
        return map;
    }
